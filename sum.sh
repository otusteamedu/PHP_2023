#!/bin/bash

if [ -z "$1" ]; then
    echo -e "\033[31m Первый аргумент отсутствует"
    exit
fi

if [ -z "$2" ]; then
    echo -e "\033[31m Второй аргумент отсутствует"
    exit
fi

if [[ ! $1 =~ ^[-]?[0-9][0-9]?[.]?[0-9]?+$ ]]; then
  echo -e "\033[31m Аргумент $1 не является числом."
  exit
fi

if [[ ! $2 =~ ^[-]?[0-9][0-9]?[.]?[0-9]?+$ ]]; then
  echo -e "\033[31m Аргумент $2 не является числом."
  exit
fi

sum=$(awk 'BEGIN {printf "%f", '"$1"' + '"$2"'}')
echo "$1 + $2 = $sum"

