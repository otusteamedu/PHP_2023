#!/bin/bash

if [ -z "$1" ] || ! [[ $1 =~ ^[+-]?([0-9]+([.][0-9]*)?|[.][0-9]+)+$ ]]; then # Проверка первого числа
  echo "Первый параметр - не число"
  exit
elif [ -z "$2" ] || ! [[ $2 =~ ^[+-]?([0-9]+([.][0-9]*)?|[.][0-9]+)+$ ]]; then # Проверка второго числа
  echo "Второй параметр - не число"
  exit
else
  sum=`echo "$1 $2" | awk '{ print $1 + $2 }' | sed 's/[,]/./g'`
  echo $sum;
fi
