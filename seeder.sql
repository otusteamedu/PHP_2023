INSERT INTO tickets (place_id, session_id)
SELECT places.id, session.id
FROM places, session
WHERE session.hall_id = places.halls_id
  AND RAND() < 0.8;

INSERT INTO orders (pay)
SELECT IF(RAND() < 0.8, TRUE, FALSE) AS pay
FROM tickets
         LIMIT 1000;

INSERT INTO basket_item (tickets_id, orders_id)
SELECT tickets.id AS tickets_id, orders.id AS orders_id
FROM tickets, orders
WHERE
    (tickets.id  < 300 AND tickets.id =  orders.id )
   OR
    (tickets.id  > 300 AND tickets.id < 600 AND tickets.id =  orders.id + 200)
   OR
    (tickets.id  > 600 AND tickets.id =  orders.id + 600)
ORDER BY orders_id;